RandomArray::RandomArray(int N, int A, int B) {
  size_ = N;
  data_ = new double[size_];
  
  for (int i=0; i<N;i++){
    data_[i]=rand() %(B-A)+A;
  }
}

// ===========================================================================
//                                 Destructor
// ===========================================================================
RandomArray::~RandomArray(){
  delete[] data_;
  }
